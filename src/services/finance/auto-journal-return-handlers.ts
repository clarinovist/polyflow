import { JournalStatus, ReferenceType } from '@prisma/client';

import { prisma } from '@/lib/core/prisma';
import { AccountingService } from '../accounting/accounting-service';

import { getAccountByCode } from './auto-journal-shared';

export async function handleSalesReturnReceived(returnId: string) {
    const salesReturn = await prisma.salesReturn.findUnique({
        where: { id: returnId },
        include: { items: true, customer: true }
    });
    if (!salesReturn || !salesReturn.totalAmount) return;

    const returnAcc = await getAccountByCode('41900');
    const arAcc = await getAccountByCode('11210');

    const lines = [
        { accountId: returnAcc.id, debit: Number(salesReturn.totalAmount), credit: 0, description: 'Sales Return' },
        { accountId: arAcc.id, debit: 0, credit: Number(salesReturn.totalAmount), description: 'AR Reversal' }
    ];

    let restockValue = 0;
    for (const item of salesReturn.items) {
        if (item.condition === 'GOOD') {
            restockValue += Number(item.returnedQty) * Number(item.unitPrice) * 0.7;
        }
    }

    if (restockValue > 0) {
        const invAcc = await getAccountByCode('11310');
        const cogsAcc = await getAccountByCode('51100');
        lines.push(
            { accountId: invAcc.id, debit: restockValue, credit: 0, description: 'Inventory Restock' },
            { accountId: cogsAcc.id, debit: 0, credit: restockValue, description: 'COGS Reversal' }
        );
    }

    await AccountingService.createJournalEntry({
        entryDate: salesReturn.returnDate,
        description: `Sales Return Credit Note ${salesReturn.returnNumber}`,
        reference: salesReturn.returnNumber,
        referenceType: ReferenceType.SALES_RETURN,
        referenceId: salesReturn.id,
        isAutoGenerated: true,
        status: JournalStatus.POSTED,
        lines
    });
}

export async function handlePurchaseReturnShipped(returnId: string) {
    const purchaseReturn = await prisma.purchaseReturn.findUnique({
        where: { id: returnId },
        include: { items: true, supplier: true }
    });
    if (!purchaseReturn || !purchaseReturn.totalAmount) return;

    const apAcc = await getAccountByCode('21110');
    const invAcc = await getAccountByCode('11310');

    await AccountingService.createJournalEntry({
        entryDate: purchaseReturn.returnDate,
        description: `Purchase Return Debit Note ${purchaseReturn.returnNumber}`,
        reference: purchaseReturn.returnNumber,
        referenceType: ReferenceType.PURCHASE_RETURN,
        referenceId: purchaseReturn.id,
        isAutoGenerated: true,
        status: JournalStatus.POSTED,
        lines: [
            { accountId: apAcc.id, debit: Number(purchaseReturn.totalAmount), credit: 0, description: 'AP Reversal / Debit Note' },
            { accountId: invAcc.id, debit: 0, credit: Number(purchaseReturn.totalAmount), description: 'Inventory Reduction' }
        ]
    });
}