// Generic Detail Journal — template-driven 2-line GL + detail table
// ---------------------------------------------------------------------------

export type DetailJournalInput = {
  type: string;
  entryDate: Date;
  description: string;
  reference?: string;
  primaryAccountId: string;
  counterAccountId: string;
  direction: DetailJournalDirection;
  details: { description: string; amount: number }[];
};

/**
 * Build 2 balanced GL lines from detail rows, respecting direction.
 *
 * OUTFLOW: Debit primary, Credit counter
 * INFLOW:  Debit counter, Credit primary
 */
export function buildDetailJournalLines(data: DetailJournalInput) {
  const total = data.details.reduce((sum, d) => sum + Number(d.amount), 0);

  if (data.direction === 'OUTFLOW') {
    return [
      {
        accountId: data.primaryAccountId,
        debit: total,
        credit: 0,
        description: data.description,
      },
      {
        accountId: data.counterAccountId,
        debit: 0,
        credit: total,
        description: data.description,
      },
    ];
  }

  // INFLOW
  return [
    {
      accountId: data.counterAccountId,
      debit: total,
      credit: 0,
      description: data.description,
    },
    {
      accountId: data.primaryAccountId,
      debit: 0,
      credit: total,
      description: data.description,
    },
  ];
}

/**
 * Create a detail journal: 2 GL lines + JournalEntryDetail rows (atomic).
 * Generic version that works for all templates (BTKL, Piutang, BPJS).
 */
export async function createDetailJournal(
  input: DetailJournalInput,
  createdById?: string,
) {
  const lines = buildDetailJournalLines(input);

  // Validate balance
  const totalDebit = lines.reduce((s, l) => s + l.debit, 0);
  const totalCredit = lines.reduce((s, l) => s + l.credit, 0);
  if (Math.abs(totalDebit - totalCredit) > 0.01) {
    throw new BusinessRuleError(
      `Journal Entry is not balanced. Debit: ${totalDebit}, Credit: ${totalCredit}`,
      { totalDebit, totalCredit },
      "UNBALANCED_JOURNAL",
    );
  }

  // Control account guard — prevent AR / Inventory accounts
  const accountIds = lines.map((l) => l.accountId);
  const controlAccounts = await prisma.account.findMany({
    where: {
      id: { in: accountIds },
      OR: [
        { code: { startsWith: "112" } }, // Accounts Receivable
        { code: { startsWith: "113" } }, // Inventory
      ],
    },
  });

  if (controlAccounts.length > 0) {
    const codes = controlAccounts
      .map((a: { code: string }) => a.code)
      .join(", ");
    throw new BusinessRuleError(
      `Manual journal entry is forbidden for Control Accounts: ${codes}. Please use the Receive Payment, Bill Payment, or Inventory modules instead to ensure data consistency.`,
      { controlAccountCodes: codes, accountIds },
    );
  }

  // Period check
  const isOpen = await isPeriodOpen(input.entryDate);
  if (!isOpen) {
    throw new BusinessRuleError(
      "Cannot create journal entry in a closed fiscal period.",
      { entryDate: input.entryDate },
      "FISCAL_PERIOD_CLOSED",
    );
  }

  const maxRetries = 3;
  for (let attempt = 0; attempt < maxRetries; attempt++) {
    try {
      return await prisma.$transaction(
        async (tx) => {
          const entryNumber = await generateEntryNumber(input.entryDate, tx);

          const entry = await tx.journalEntry.create({
            data: {
              entryNumber,
              entryDate: input.entryDate,
              description: input.description,
              reference: input.reference ?? "",
              referenceType: "MANUAL_ENTRY",
              status: JournalStatus.DRAFT,
              isAutoGenerated: false,
              createdById,
              lines: {
                create: lines.map((l) => ({
                  accountId: l.accountId,
                  debit: l.debit,
                  credit: l.credit,
                  description: l.description,
                })),
              },
              details: {
                create: input.details.map((d, idx) => ({
                  type: input.type,
                  description: d.description,
                  amount: d.amount,
                  sortOrder: idx,
                })),
              },
            },
            include: {
              lines: { include: { account: true } },
              details: { orderBy: { sortOrder: "asc" } },
            },
          });

          return entry;
        },
        { isolationLevel: Prisma.TransactionIsolationLevel.Serializable },
      );
    } catch (error) {
      if (
        error instanceof Prisma.PrismaClientKnownRequestError &&
        error.code === "P2002" &&
        attempt < maxRetries - 1
      ) {
        continue;
      }
      throw error;
    }
  }

  throw new BusinessRuleError(
    `Failed to allocate unique journal entryNumber after ${maxRetries} attempts`,
    {},
    "ENTRY_NUMBER_ALLOCATION_FAILED",
  );
}

/**
 * Update a DRAFT detail journal: replace lines + details atomically.
 * Generic version that works for all templates.
 */
export async function updateDetailJournal(
  id: string,
  input: DetailJournalInput,
  _userId?: string,
) {
  // 1. Load existing
  const existing = await prisma.journalEntry.findUnique({
    where: { id },
    include: { lines: true, details: true },
  });

  if (!existing) throw new NotFoundError("Journal", id);

  // 2. Guard: must be DRAFT
  if (existing.status !== JournalStatus.DRAFT) {
    throw new BusinessRuleError(
      "Hanya jurnal DRAFT yang bisa diedit",
      { journalId: id, currentStatus: existing.status },
      "INVALID_STATUS_TRANSITION",
    );
  }

  // 3. Guard: not auto-generated
  if (existing.isAutoGenerated) {
    throw new BusinessRuleError(
      "Jurnal otomatis tidak bisa diedit. Ubah dokumen sumber.",
      { journalId: id, isAutoGenerated: true },
    );
  }

  // 4. Balance check
  const lines = buildDetailJournalLines(input);
  const totalDebit = lines.reduce((s, l) => s + l.debit, 0);
  const totalCredit = lines.reduce((s, l) => s + l.credit, 0);

  if (Math.abs(totalDebit - totalCredit) > 0.01) {
    throw new BusinessRuleError(
      `Journal Entry is not balanced. Debit: ${totalDebit}, Credit: ${totalCredit}`,
      { totalDebit, totalCredit, journalId: id },
      "UNBALANCED_JOURNAL",
    );
  }

  // 5. Control account check
  const accountIds = lines.map((l) => l.accountId);
  const controlAccounts = await prisma.account.findMany({
    where: {
      id: { in: accountIds },
      OR: [
        { code: { startsWith: "112" } },
        { code: { startsWith: "113" } },
      ],
    },
  });

  if (controlAccounts.length > 0) {
    const codes = controlAccounts.map((a) => a.code).join(", ");
    throw new BusinessRuleError(
      `Manual journal entry is forbidden for Control Accounts: ${codes}.`,
      { controlAccountCodes: codes, accountIds, journalId: id },
    );
  }

  // 6. Period open check
  const isOpen = await isPeriodOpen(input.entryDate);
  if (!isOpen) {
    throw new BusinessRuleError(
      "Periode fiskal sudah ditutup",
      { journalId: id, entryDate: input.entryDate },
      "FISCAL_PERIOD_CLOSED",
    );
  }

  // 7. Atomic update
  return await prisma.$transaction(
    async (tx) => {
      // Update header
      await tx.journalEntry.update({
        where: { id },
        data: {
          entryDate: input.entryDate,
          description: input.description,
          reference: input.reference,
        },
      });

      // Replace GL lines
      await tx.journalLine.deleteMany({ where: { journalEntryId: id } });
      await tx.journalLine.createMany({
        data: lines.map((l) => ({
          journalEntryId: id,
          accountId: l.accountId,
          debit: l.debit,
          credit: l.credit,
          description: l.description,
        })),
      });

      // Replace details
      await tx.journalEntryDetail.deleteMany({
        where: { journalEntryId: id },
      });
      await tx.journalEntryDetail.createMany({
        data: input.details.map((d, idx) => ({
          journalEntryId: id,
          type: input.type,
          description: d.description,
          amount: d.amount,
          sortOrder: idx,
        })),
      });

      // Return updated journal
      return await tx.journalEntry.findUnique({
        where: { id },
        include: {
          lines: { include: { account: true } },
          details: { orderBy: { sortOrder: "asc" } },
        },
      });
    },
    { isolationLevel: Prisma.TransactionIsolationLevel.Serializable },
  );
}
