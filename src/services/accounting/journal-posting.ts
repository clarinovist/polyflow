export async function createJournalEntry(
  input: CreateJournalEntryInput,
  tx?: Prisma.TransactionClient,
) {
  const { lines } = input;

  const totalDebit = lines.reduce((sum, line) => sum + Number(line.debit), 0);
  const totalCredit = lines.reduce((sum, line) => sum + Number(line.credit), 0);

  if (Math.abs(totalDebit - totalCredit) > 0.01) {
    throw new BusinessRuleError(
      `Journal Entry is not balanced. Debit: ${totalDebit}, Credit: ${totalCredit}`,
      { totalDebit, totalCredit, difference: Math.abs(totalDebit - totalCredit) },
      "UNBALANCED_JOURNAL",
    );
  }

  // Validation: Prevent manual entries to Control Accounts (AR & Inventory)
  if (!input.isAutoGenerated && input.referenceType === "MANUAL_ENTRY") {
    const db = tx || prisma;
    const accountIds = lines.map((l) => l.accountId);
    const controlAccounts = await db.account.findMany({
      where: {
        id: { in: accountIds },
        OR: [
          { code: { startsWith: "112" } }, // Accounts Receivable
          { code: { startsWith: "113" } }, // Inventory
        ],
      },
    });

    if (controlAccounts.length > 0) {
      const codes = controlAccounts.map((a: {code: string}) => a.code).join(", ");
      throw new BusinessRuleError(
        `Manual journal entry is forbidden for Control Accounts: ${codes}. Please use the Receive Payment, Bill Payment, or Inventory modules instead to ensure data consistency.`,
        { controlAccountCodes: codes, accountIds },
      );
    }
  }

  const isOpen = await isPeriodOpen(input.entryDate);
  if (!isOpen) {
    throw new BusinessRuleError(
      "Cannot create journal entry in a closed fiscal period.",
      { entryDate: input.entryDate },
      "FISCAL_PERIOD_CLOSED",
    );
  }

  const maxRetries = 3;
  let lastError: unknown;

  for (let i = 0; i < maxRetries; i++) {
    try {
      const executeInTx = async (db: Prisma.TransactionClient) => {
        const entryNumber = await generateEntryNumber(input.entryDate, db);

        return await db.journalEntry.create({
          data: {
            entryNumber,
            entryDate: input.entryDate,
            description: input.description,
            reference: input.reference,
            referenceType: input.referenceType,
            referenceId: input.referenceId,
            status: input.status || JournalStatus.DRAFT,
            isAutoGenerated: input.isAutoGenerated || false,
            createdById: input.createdById,
            lines: {
              create: lines.map((line) => ({
                accountId: line.accountId,
                debit: line.debit,
                credit: line.credit,
                description: line.description,
              })),
            },
          },
          include: {
            lines: {
              include: { account: true },
            },
          },
        });
      };

      if (tx) {
        return await executeInTx(tx);
      } else {
        return await prisma.$transaction(
          async (innerTx) => {
            return await executeInTx(innerTx);
          },
          {
            isolationLevel: Prisma.TransactionIsolationLevel.Serializable,
          },
        );
      }
    } catch (error) {
      lastError = error;
      if (
        error instanceof Prisma.PrismaClientKnownRequestError &&
        error.code === "P2002"
      ) {
        const target = (error.meta?.target as string[]) || [];
        if (target.includes("entryNumber") && !tx) {
          console.warn(
            `[JournalService] entryNumber collision detected, retrying... (${i + 1}/${maxRetries})`,
          );
          await new Promise((resolve) => setTimeout(resolve, 50 * (i + 1)));
          continue;
        }
      }
      throw error;
    }
  }

  throw lastError;
}

export async function postJournal(
  id: string,
  userId?: string,
  tx?: Prisma.TransactionClient,
) {
  const db = tx || prisma;
  const journal = await db.journalEntry.findUnique({
    where: { id },
    include: { lines: true },
  });

  if (!journal) throw new NotFoundError("Journal", id);
  if (journal.status !== "DRAFT")
    throw new BusinessRuleError(
      "Only DRAFT journals can be posted",
      { journalId: id, currentStatus: journal.status },
      "INVALID_STATUS_TRANSITION",
    );

  const isOpen = await isPeriodOpen(journal.entryDate, tx);
  if (!isOpen) {
    throw new BusinessRuleError(
      "Cannot post journal entry to a closed fiscal period.",
      { journalId: id, entryDate: journal.entryDate },
      "FISCAL_PERIOD_CLOSED",
    );
  }

  const updatedJournal = await db.journalEntry.update({
    where: { id },
    data: {
      status: "POSTED",
      approvedById: userId,
      approvedAt: new Date(),
    },
  });

  // Cascade status update to the source document if applicable
  if (journal.referenceType === "PURCHASE_INVOICE" && journal.referenceId) {
    await db.purchaseInvoice.updateMany({
      where: { id: journal.referenceId, status: "DRAFT" },
      data: { status: "UNPAID" },
    });
  } else if (journal.referenceType === "SALES_INVOICE" && journal.referenceId) {
    await db.invoice.updateMany({
      where: { id: journal.referenceId, status: "DRAFT" },
      data: { status: "UNPAID" },
    });
  }

  return updatedJournal;
}

export async function postBulkJournals(ids: string[], userId?: string) {
  const journals = await prisma.journalEntry.findMany({
    where: { id: { in: ids }, status: "DRAFT" },
  });

  for (const journal of journals) {
    const isOpen = await isPeriodOpen(journal.entryDate);
    if (!isOpen) {
      throw new BusinessRuleError(
        `Cannot post journal ${journal.entryNumber} to a closed fiscal period.`,
        { journalId: journal.id, entryNumber: journal.entryNumber, entryDate: journal.entryDate },
        "FISCAL_PERIOD_CLOSED",
      );
    }
  }

  const res = await prisma.journalEntry.updateMany({
    where: {
      id: { in: ids },
      status: "DRAFT",
    },
    data: {
      status: "POSTED",
      approvedById: userId,
      approvedAt: new Date(),
    },
  });

  const purchaseInvoiceIds = journals
    .filter((j) => j.referenceType === "PURCHASE_INVOICE" && j.referenceId)
    .map((j) => j.referenceId as string);

  const salesInvoiceIds = journals
    .filter((j) => j.referenceType === "SALES_INVOICE" && j.referenceId)
    .map((j) => j.referenceId as string);

  if (purchaseInvoiceIds.length > 0) {
    await prisma.purchaseInvoice.updateMany({
      where: { id: { in: purchaseInvoiceIds }, status: "DRAFT" },
      data: { status: "UNPAID" },
    });
  }

  if (salesInvoiceIds.length > 0) {
    await prisma.invoice.updateMany({
      where: { id: { in: salesInvoiceIds }, status: "DRAFT" },
      data: { status: "UNPAID" },
    });
  }

  return res;
}

export async function reverseJournal(
  id: string,
  userId?: string,
  reason?: string,
) {
  const reversedById = userId || "system";
  const journal = await prisma.journalEntry.findUnique({
    where: { id },
    include: { lines: true },
  });

  if (!journal) throw new NotFoundError("Journal", id);
  if (journal.status !== "POSTED")
    throw new BusinessRuleError(
      "Only POSTED journals can be reversed",
      { journalId: id, currentStatus: journal.status },
      "INVALID_STATUS_TRANSITION",
    );
  if (journal.isAutoGenerated)
    throw new BusinessRuleError(
      "Cannot directly reverse an auto-generated journal",
      { journalId: id, isAutoGenerated: true },
    );

  const isOpen = await isPeriodOpen(new Date());
  if (!isOpen) {
    throw new BusinessRuleError(
      "Cannot reverse journal in a closed fiscal period.",
      { journalId: id },
      "FISCAL_PERIOD_CLOSED",
    );
  }

  const entryNumber = await generateEntryNumber(new Date());

  const reversal = await prisma.journalEntry.create({
    data: {
      entryNumber,
      entryDate: new Date(),
      description: `REVERSAL of ${journal.entryNumber}: ${reason || journal.description}`,
      reference: journal.entryNumber,
      referenceType: "MANUAL_ENTRY",
      status: "POSTED",
      isAutoGenerated: false,
      createdById: reversedById,
      approvedById: reversedById,
      approvedAt: new Date(),
      lines: {
        create: journal.lines.map((line) => ({
          accountId: line.accountId,
          debit: line.credit, // Swap debit/credit
          credit: line.debit,
          description: `Reversal: ${line.description}`,
        })),
      },
    },
    include: {
      lines: { include: { account: true } },
    },
  });

  await prisma.journalEntry.update({
    where: { id },
    data: { status: "VOIDED" },
  });

  return reversal;
}

