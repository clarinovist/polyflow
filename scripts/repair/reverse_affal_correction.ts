// @ts-nocheck
import { PrismaClient, MovementType, JournalStatus } from '@prisma/client';

const prisma = new PrismaClient();

async function getAccountId(code: string): Promise<string> {
    const acc = await prisma.account.findUnique({ where: { code } });
    if (!acc) throw new Error(`GL Account code ${code} not found during auto-journal.`);
    return acc.id;
}

async function main() {
    const transactionId = '031a519a-7c9b-4915-a01a-6c3406b954d6';
    const qtyToReverse = 65.61;
    const productVariantId = '0635d93b-ffbe-403d-8a05-bb2835cf4d73';
    const locationId = '0c882912-ebec-44e4-8431-bae328a11436';

    console.log(`Starting reversal of transaction ${transactionId}...`);

    await prisma.$transaction(async (tx) => {
        // 1. Fetch Product details for accounting
        const productVariant = await tx.productVariant.findUniqueOrThrow({
            where: { id: productVariantId },
            include: { product: true }
        });

        // 2. Increment Inventory
        await tx.inventory.update({
            where: {
                locationId_productVariantId: {
                    locationId,
                    productVariantId,
                },
            },
            data: {
                quantity: { increment: qtyToReverse },
            },
        });

        // 3. Create Reversal Stock Movement
        const reference = `REVERSAL: CORRECTION Missed Affal Backflush WO#WO-260303-EXT17`;
        const movement = await tx.stockMovement.create({
            data: {
                type: MovementType.IN,
                productVariantId,
                toLocationId: locationId,
                quantity: qtyToReverse,
                reference,
                createdAt: new Date(),
            },
        });

        console.log(`Created reversal movement: ${movement.id}`);

        // 4. Manual Accounting Record (Minimal implementation of recordInventoryMovement)
        // For 'IN' movement without goodsReceiptId
        const currentCost = Number(productVariant.standardCost || productVariant.buyPrice || productVariant.price || 0);
        const totalAmount = qtyToReverse * currentCost;

        if (totalAmount > 0) {
            const invAccountCode = productVariant.product.inventoryAccountId ?
                (await tx.account.findUnique({ where: { id: productVariant.product.inventoryAccountId } }))?.code || '11320' :
                '11320'; // Fallback to WIP since we saw it mapped to 11320 earlier

            const lines = [
                { accountId: (await getAccountId(invAccountCode)), debit: totalAmount, credit: 0, description: `Reverse Correction: ${productVariant.name}` },
                { accountId: (await getAccountId('81100')), debit: 0, credit: totalAmount, description: `Inventory Recovery` }
            ];

            const entryNumber = `JE-REPAIR-${Date.now()}`;
            await tx.journalEntry.create({
                data: {
                    entryNumber,
                    entryDate: new Date(),
                    description: `Auto: REVERSAL - ${productVariant.name}`,
                    reference: movement.id,
                    referenceType: 'MANUAL_ENTRY',
                    referenceId: movement.id,
                    isAutoGenerated: true,
                    status: JournalStatus.POSTED,
                    lines: {
                        create: lines
                    }
                }
            });
            console.log(`Created journal entry for ${totalAmount} IDR`);
        }
    });

    console.log('Reversal completed successfully.');
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(async () => {
        await prisma.$disconnect();
    });
